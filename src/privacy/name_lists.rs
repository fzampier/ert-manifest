static SURNAME_SET: Lazy<HashSet<&'static str>> = Lazy::new(|| {
    SURNAMES.iter().copied().collect()
});

static FIRST_NAME_SET: Lazy<HashSet<&'static str>> = Lazy::new(|| {
    FIRST_NAMES.iter().copied().collect()
});

/// Check if a value looks like a person's name
/// Returns true if:
/// - The value is a known first name or surname
/// - The value is a "Firstname Lastname" pattern where either part is recognized
/// - The value is a "Firstname Middle Lastname" pattern with recognized first/last
pub fn is_likely_name(value: &str) -> bool {
    let value_lower = value.trim().to_lowercase();
    
    // Empty or single char - not a name
    if value_lower.len() < 2 {
        return false;
    }
    
    // Check single word against both lists
    if SURNAME_SET.contains(value_lower.as_str()) || FIRST_NAME_SET.contains(value_lower.as_str()) {
        return true;
    }
    
    // Check "Firstname Lastname" pattern
    let parts: Vec<&str> = value_lower.split_whitespace().collect();
    if parts.len() == 2 {
        let first = parts[0];
        let last = parts[1];
        
        // Both parts should be alphabetic and reasonable length
        if first.chars().all(|c| c.is_alphabetic() || c == '-') 
            && last.chars().all(|c| c.is_alphabetic() || c == '-')
            && first.len() >= 2 && first.len() <= 20
            && last.len() >= 2 && last.len() <= 25
        {
            // Match if either part is a known name
            if FIRST_NAME_SET.contains(first) || SURNAME_SET.contains(last) {
                return true;
            }
            // Also match if first part is a known surname (handles "Lastname Firstname")
            if SURNAME_SET.contains(first) || FIRST_NAME_SET.contains(last) {
                return true;
            }
        }
    }
    
    // Check "Firstname Middle Lastname" pattern
    if parts.len() == 3 {
        let first = parts[0];
        let last = parts[2];
        
        if first.chars().all(|c| c.is_alphabetic() || c == '-') 
            && last.chars().all(|c| c.is_alphabetic() || c == '-')
            && first.len() >= 2 && last.len() >= 2
        {
            if (FIRST_NAME_SET.contains(first) && SURNAME_SET.contains(last))
                || (SURNAME_SET.contains(first) && FIRST_NAME_SET.contains(last))
            {
                return true;
            }
        }
    }
    
    false
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_single_surname() {
        assert!(is_likely_name("Smith"));
        assert!(is_likely_name("JOHNSON"));
        assert!(is_likely_name("silva"));
        assert!(is_likely_name("Tremblay"));
    }

    #[test]
    fn test_single_first_name() {
        assert!(is_likely_name("John"));
        assert!(is_likely_name("MARIA"));
        assert!(is_likely_name("jean"));
        // Canadian Census names
        assert!(is_likely_name("Aaliyah"));
        assert!(is_likely_name("Muhammad"));
        assert!(is_likely_name("Wei"));
    }

    #[test]
    fn test_full_name() {
        assert!(is_likely_name("John Smith"));
        assert!(is_likely_name("Mary Johnson"));
        assert!(is_likely_name("Jose Silva"));
        assert!(is_likely_name("Marie Tremblay"));
    }

    #[test]
    fn test_full_name_with_middle() {
        assert!(is_likely_name("John Robert Smith"));
        assert!(is_likely_name("Maria Ana Silva"));
    }

    #[test]
    fn test_hyphenated_names() {
        assert!(is_likely_name("Jean-Pierre"));
        assert!(is_likely_name("Mary-Jane Smith"));
    }

    #[test]
    fn test_not_names() {
        assert!(!is_likely_name("Treatment"));
        assert!(!is_likely_name("Control"));
        assert!(!is_likely_name("42"));
        assert!(!is_likely_name("2024-01-15"));
        assert!(!is_likely_name("A1B2C3"));
        assert!(!is_likely_name("ABCD1234"));
    }

    #[test]
    fn test_edge_cases() {
        assert!(!is_likely_name(""));
        assert!(!is_likely_name("   "));
        assert!(!is_likely_name("X"));  // Too short
    }
    
    #[test]
    fn test_adversarial_names() {
        // Names that were missed in the adversarial test
        assert!(is_likely_name("Mary Smith"));
        assert!(is_likely_name("Robert Jones"));
        assert!(is_likely_name("Patricia Williams"));
    }
}

